    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        
        String cod= JOptionPane.showInputDialog(" Digite el codigo del Trabajador a eliminar");
        boolean est = jfrmpri.eliminarTrabajador(cod);
        if(est!=false){
            
            JOptionPane.showMessageDialog(null, " El Trabajador ha sido borrado correctamente");
        } else{
            
            JOptionPane.showMessageDialog(null, " El codigo que ingreso no existe. El Trabajador no ha sido borado intente nuevamente.");
        }
        jfrmpri.SetllenarJTable(jTable1);
    }//GEN-LAST:event_jMenuItem4ActionPerformed

