    private void jMenuItem7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem7ActionPerformed
       // busqueda binaria 
       String matri=JOptionPane.showInputDialog(" Digite la matricula del auto que desea buscar");
       // capturamos la posicion
       int pos=jfrpri.getBusqueda_Bi(matri);
       if(pos==-1){
           
           JOptionPane.showMessageDialog(null," los sentimos la matricula del vehiculo no ha sido encontrada");
       }else{
           
           jfrpri.v[pos].mostrar();
       }
    }//GEN-LAST:event_jMenuItem7ActionPerformed

