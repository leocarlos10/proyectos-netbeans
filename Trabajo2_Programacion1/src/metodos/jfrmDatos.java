    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed

        boolean estado = false;
        try{
        estado=datos.jfrmp.setAddinfo(codigo, nombre.getText(), Float.parseFloat(precio.getText()), Integer.parseInt(cantidad.getText()),i);
        } catch(NumberFormatException e){
              JOptionPane.showMessageDialog(null," Error !. Porfavor revise los datos de ingreso");
          }
        if(estado!= false){
            i++; 
            // ya que todos los vectores tinen el mismo tama√±o solo basta con que la i sea menor a uno de ellos eso quiere decir que debe seguir agregando datos.
            if((i<datos.jfrmp.codigoProducto.length)){
                
                info.setText(" Ingrese la informacion en la posicion: "
                        + ""+(i+1));
                codigo.setText("");
                nombre.setText("");
                precio.setText("");
                cantidad.setText("");
                codigo.requestFocus();
            } else{
            
                info.setText(" Ha ingresado todos los datos !!");
                codigo.setText("");
                nombre.setText("");
                precio.setText("");
                cantidad.setText("");
                codigo.setEditable(false);
                nombre.setEditable(false);
                precio.setEditable(false);
                cantidad.setEditable(false);
                agregar.setEnabled(false);
                cerrar.setEnabled(true);
                cerrar.requestFocus();
            }
            
            datos.jfrmp.setllenarJTable(datos.jTable1);
        }
    }//GEN-LAST:event_agregarActionPerformed

    
